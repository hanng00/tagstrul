const stationsByName = new Map<string, Station>();
const stationsByUic = new Map<string, Station>();

for (const station of stations) {
  stationsByName.set(station.name.toLowerCase(), station);
  stationsByUic.set(station.uic, station);
}

export function getStationByName(name: string): Station | undefined {
  return stationsByName.get(name.toLowerCase());
}

export function getStationByUic(uic: string): Station | undefined {
  return stationsByUic.get(uic);
}

export function normalizeStationName(input: string): string | undefined {
  const station = stationsByName.get(input.toLowerCase());
  return station?.name;
}

export function searchStations(query: string, limit = 10): Station[] {
  const q = query.toLowerCase();
  return stations
    .filter(s => s.name.toLowerCase().includes(q) || s.shortName.toLowerCase().includes(q))
    .slice(0, limit);
}
